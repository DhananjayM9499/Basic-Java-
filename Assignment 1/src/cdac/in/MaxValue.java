package cdac.in;

public class MaxValue {

	public static void main(String[] args) {
		int maxValue = Integer.MAX_VALUE;
		double dmaxValue = Double.MAX_VALUE;
		int minValue = Integer.MIN_VALUE;
		double dminValue = Double.MIN_VALUE;
		float fmaxValue = Float.MAX_VALUE;
		float fminValue = Float.MIN_VALUE;
		byte bmaxValue = Byte.MAX_VALUE;
		byte bminValue = Byte.MIN_VALUE;
		char cmaxValue = Character.MAX_VALUE;
		char cminValue = Character.MIN_VALUE;
		System.out.println("Maximum value of int is "+ maxValue);
		System.out.println("Maximum value of double is "+ dmaxValue);
		System.out.println("Minimum value of int is "+ minValue);
		System.out.println("Minimum value of double is "+ dminValue);
		System.out.println("Maximum value of float is "+ fmaxValue);
		System.out.println("Minimum value of float is "+ fminValue);
		System.out.println("Maximum value of byte is "+ bmaxValue);
		System.out.println("Minimum value of byte is "+ bminValue);
		System.out.println("Maximum value of char is "+ cmaxValue);
		System.out.println("Minimum value of char is "+ cminValue);
		
	}

}
